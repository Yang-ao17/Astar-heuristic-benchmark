import numpy as np


def h_zero(a, b):
    """Dijkstra-like baseline: A* becomes uninformed search because h = 0."""
    return 0.0


def h_manhattan(a, b):
    """Manhattan distance.

    This is natural for 4-neighbor grids, but it can be non-admissible when
    diagonal movement is allowed because it may overestimate the true cost.
    """
    return abs(a[0] - b[0]) + abs(a[1] - b[1])


def h_euclidean(a, b):
    """Straight-line distance, admissible for 8-neighbor movement."""
    dx = a[0] - b[0]
    dy = a[1] - b[1]
    return np.sqrt(dx * dx + dy * dy)


def h_octile(a, b):
    """Octile distance, usually suitable for 8-neighbor grid movement."""
    dx = abs(a[0] - b[0])
    dy = abs(a[1] - b[1])
    return max(dx, dy) + (np.sqrt(2) - 1) * min(dx, dy)


def h_weighted_euclidean(a, b):
    """Toy non-admissible candidate: a more aggressive Euclidean heuristic."""
    return 1.2 * h_euclidean(a, b)


def h_llm_candidate(a, b):
    """Toy placeholder for an LLM-suggested heuristic.

    This is not generated by an API call. It is a simple example of a candidate
    heuristic that could be produced, modified, and evaluated automatically.
    """
    return 0.6 * h_octile(a, b) + 0.4 * h_euclidean(a, b)


def h_misleading(a, b):
    """Deliberately poor toy non-admissible heuristic.

    This gives lower scores to nodes farther from the goal, so it can waste the
    expansion budget. It helps demonstrate how the benchmark detects weak
    heuristic candidates.
    """
    return -1.5 * h_manhattan(a, b)


HEURISTICS = {
    "zero": h_zero,
    "manhattan": h_manhattan,
    "euclidean": h_euclidean,
    "octile": h_octile,
    "weighted_euclidean": h_weighted_euclidean,
    "llm_candidate": h_llm_candidate,
    "misleading": h_misleading,
}
